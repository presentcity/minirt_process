#include "minirt.h"

int			rgb_to_color(t_vec3f *rgb)
{
	int color;

	color = (int)(((int)rgb->x));
	color = (int)((color << 8) + (int)rgb->y);
	color = (int)((color << 8) + (int)rgb->z);
	return (color);
}

